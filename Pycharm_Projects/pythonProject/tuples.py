a = (1, 2, 3, 4, 5)

# Accessing elements using Index

print(a[2])

# Slicing

print(a[1:])
print(a[:1])
print(a[::-1])
print(a[1:4])

# Tuple Length

print(len(a))

# Tuple Concatenation

b = (6, 7, 8)
print(a + b)

# Tuple Repetition

c = b*5
print(c)

# Tuple Unpacking

b = ("Rahul", "Joe", 35 )
first, name, age = b
print(first)
print(name)
print(age)

