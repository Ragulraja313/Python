// If Statement
let num = 10;

if (num > 0) {
    console.log("Number is positive");
}

// Else Block
let num1 = -5;

if (num1 > 0) {
    console.log("Number is positive");
} else {
    console.log("Number is non-positive");
}

// Else if Block
let num2 = 0;

if (num2 > 0) {
    console.log("Number is positive");
} else if (num2 < 0) {
    console.log("Number is negative");
} else {
    console.log("Number is zero");
}
